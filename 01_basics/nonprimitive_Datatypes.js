// Non Primitive Datatypes

// Array

let Car = ["BMW", "Honda", "Mercedes", "Toyota"]; // Datatypes = Array
console.log(typeof Car); // Return = Object
console.log(Car);

const classroom = {   // Datatypes = Object
    name: "Ahmad",
    rollno: 12,
}
console.log(classroom.name);
console.log(classroom.rollno);
console.log(typeof classroom); // Return = Object